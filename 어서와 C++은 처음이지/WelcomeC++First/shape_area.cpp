#include <iostream>
#define PI 3.141592//2
using namespace std;

int main()
{
	int choice, area, length, width; //1
	int radius; //2
	int length2, width2;//3
	cout << "도형을 선택하시오(1, 2, 3): ";
	cin >> choice;
	if (choice == 1) //사각형
	{
		cout << "가로 : ";
		cin >> length;
		cout << "세로 : ";
		cin >> width;
		area = length * width;
		cout << "면적 : " << area << endl;
	}
	else if (choice == 2) //원
	{
		cout << "반지름 : ";
		cin >> radius;
		area = (radius * radius)*PI;
		cout << "면적 : " << area << endl;
	}
	else if (choice == 3)//삼각형
	{
		cout << "가로 : ";
		cin >> length2;
		cout << "세로 : ";
		cin >> width2;
		area = (length2 * width2)*0.5;
		cout << "면적 : " << area << endl;
	}
	else
	{
		cout << "올바르지 못한 선택입니다." << endl;
	}
	return 0;
}